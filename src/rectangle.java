public class rectangle{
    int length = 5;
    int height = 10;

    public String rectArea(){
        return ("rectangle " + length + " x " + height + " feet area " + length * height);
    }
    public String rectPerimeter(){
        return ("Square " + length + " x " + length + " feet Perimeter " + (length * 2) * 2);
    }
}
